// 현재 언어 반환
function getLang() {
  return localStorage.getItem('dqa_lang') || 'ko';
}

// 키로 번역 문자열 반환 (없으면 ko fallback)
function t(key) {
  const lang = getLang();
  return (I18N[lang] && I18N[lang][key] !== undefined)
    ? I18N[lang][key]
    : (I18N.ko[key] !== undefined ? I18N.ko[key] : key);
}

// 언어 변경 후 페이지 새로고침
function setLang(lang) {
  localStorage.setItem('dqa_lang', lang);
  location.reload();
}
